Deno.test("supports anyOf and oneOf unions", () => {
  const anyOfOutput = generateFromSchema(
    {
      type: "object",
      properties: {
        value: {
          anyOf: [{ type: "string" }, { type: "integer" }],
        },
      },
    },
    {},
    { banner: false },
  );
  assert.match(anyOfOutput, /---@field value\? string\|integer/);

  const oneOfOutput = generateFromSchema(
    {
      type: "object",
      properties: {
        value: {
          oneOf: [{ const: "on" }, { const: "off" }],
        },
      },
    },
    {},
    { banner: false },
  );
  assert.match(oneOfOutput, /---@field value\? "on"\|"off"/);
});

Deno.test("strict mode rejects unsupported keywords", () => {
  assert.throws(
    () => {
      convertSchema({
        type: "integer",
        minimum: 0,
      }, { strict: true });
    },
    {
      message: "Unsupported keyword in strict mode: minimum",
    },
  );
});

Deno.test("non-strict mode ignores unsupported keywords", () => {
  const output = generateFromSchema(
    {
      type: "integer",
      minimum: 0,
    },
    {},
    { banner: false },
  );

  assert.equal(output.trim(), "---@type integer");
});

Deno.test("loads fixture file and emits deterministic banner output", async () => {
  const schema = JSON.parse(await readFile(exampleSchemaPath, "utf8"));
  const converted = convertSchema(schema, { rootName: "User" });
  const output = emitLua(converted, { banner: true, version: VERSION });

  assert.match(
    output,
    new RegExp(`--- Generated by @ras0q/jsonschema2lua ${VERSION.replaceAll(".", "\\.")}`),
  );
  assert.match(output, /---@class User/);
  assert.match(output, /---@field roles\? string\[\]/);
});

