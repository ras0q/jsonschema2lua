import assert from "node:assert/strict";
import { readFile } from "node:fs/promises";
import { join } from "node:path";
import { fileURLToPath } from "node:url";
import { generateFromSchema } from "../mod.ts";
import { convertSchema } from "./convert.ts";
import { emitLua } from "./emit_lua.ts";
import { VERSION } from "./version.ts";

const exampleSchemaPath = join(
  fileURLToPath(new URL(".", import.meta.url)),
  "../examples/user.schema.json",
);

Deno.test("converts object schema with required and optional fields", () => {
  const output = generateFromSchema(
    {
      title: "User",
      type: "object",
      required: ["id", "name"],
      properties: {
        id: { type: "integer" },
        name: { type: "string" },
        email: { type: "string" },
      },
    },
    {},
    { banner: false },
  );

  assert.match(output, /---@class User/);
  assert.match(output, /---@field id integer/);
  assert.match(output, /---@field name string/);
  assert.match(output, /---@field email\? string/);
});

Deno.test("converts arrays, maps, enums, and nullable unions", () => {
  const output = generateFromSchema(
    {
      type: "object",
      properties: {
        tags: {
          type: "array",
          items: { type: "string" },
        },
        metadata: {
          type: "object",
          additionalProperties: { type: "string" },
        },
        status: {
          enum: ["draft", "published", "archived"],
        },
        deletedAt: {
          type: ["string", "null"],
        },
      },
    },
    {},
    { banner: false },
  );

  assert.match(output, /---@field tags\? string\[\]/);
  assert.match(output, /---@field metadata\? table<string, string>/);
  assert.match(
    output,
    /---@field status\? "draft"\|"published"\|"archived"/,
  );
  assert.match(output, /---@field deletedAt\? string\|nil/);
});

Deno.test("converts $defs and internal $ref", () => {
  const output = generateFromSchema(
    {
      type: "object",
      title: "Post",
      properties: {
        author: { $ref: "#/$defs/User" },
      },
      $defs: {
        User: {
          type: "object",
          properties: {
            id: { type: "integer" },
          },
        },
      },
    },
    {},
    { banner: false },
  );

  assert.match(output, /---@class Post/);
  assert.match(output, /---@field author\? User/);
  assert.match(output, /---@class User/);
  assert.match(output, /---@field id\? integer/);
});

